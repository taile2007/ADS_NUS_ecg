	public static double []conv( double []Signal, int len_signal, double []Hn, int len_Hn)
	{
		double []Result = new double[len_signal + len_Hn - 1];
	  	int n;

	  for (n = 0; n < len_signal + len_Hn - 1; n++)
	  {
			int kmin, kmax, k;

			Result[n] = 0;

			kmin = (n >= len_Hn - 1) ? n - (len_Hn - 1) : 0;
			kmax = (n < len_signal - 1) ? n : len_signal - 1;

			for (k = kmin; k <= kmax; k++)
			{
			  Result[n] += Signal[k] * Hn[n - k];
			}
	  }
	  return Result;
	  }

